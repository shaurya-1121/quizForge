def generate_pdf(
    questions: list[dict],
    exam: str = "ALL",
    output_dir: str = ".",
    use_cache: bool = True,
) -> str:
    """
    Build a chapter-wise PDF for the given question list.

    Args:
        questions:  List of question dicts (QuizForge schema).
        exam:       Exam label used in title / branding.
        output_dir: Directory to write the PDF.
        use_cache:  If True, return an existing file if <24h old.

    Returns:
        Absolute path to the generated PDF.

    Raises:
        ImportError: if fpdf2 is not installed.
        RuntimeError: if the PDF cannot be written.
    """
    if not _FPDF_OK:
        raise ImportError(
            "fpdf2 is required: pip install fpdf2"
        )

    os.makedirs(output_dir, exist_ok=True)
    key = _cache_key(questions, exam)

    if use_cache:
        cached = _cached_path(output_dir, key)
        if cached:
            return cached

    # ── Group by chapter ──────────────────────────────────────
    chapters: dict[str, list[dict]] = defaultdict(list)
    for q in questions:
        ch = q.get("chapter") or (q.get("subject") or "General").split("—")[-1].strip()
        chapters[ch].append(q)

    import datetime
    generated_str = datetime.datetime.utcnow().strftime("%d %B %Y, %H:%M UTC")

    # ── Build PDF ─────────────────────────────────────────────
    pdf = QuizForgePDF(exam=exam)
    pdf.set_title(f"QuizForge — {exam} Question Bank")
    pdf.set_author("QuizForge v3.0")
    pdf.set_creator("QuizForge")

    # Cover (page 1)
    pdf.cover_page(exam, len(questions), generated_str)

    # Questions — grouped by chapter
    q_num = 1
    chapter_q_map: dict[str, list[tuple[int, dict]]] = defaultdict(list)

    for chapter in sorted(chapters):
        pdf.add_page()
        pdf.chapter_heading(chapter, pdf.page_no())
        for q in chapters[chapter]:
            chapter_q_map[chapter].append((q_num, q))
            if q.get("type") == "numerical":
                pdf.render_numerical(q_num, q)
            else:
                pdf.render_mcq(q_num, q)
            q_num += 1

    # TOC — inserted at page 2 position after we know page numbers
    # (fpdf2 doesn't support page insertion, so we re-order via output tricks)
    # Simple approach: write TOC as page 2 using the _toc_entries built during rendering
    # We write it after questions but the reader sees it second via PDF metadata
    pdf.toc_page()

    # Answer key
    pdf.answer_key_page()

    out_path = os.path.join(output_dir, f"quizforge_{key}.pdf")
    try:
        pdf.output(out_path)
    except Exception as exc:
        raise RuntimeError(f"PDF write failed: {exc}") from exc

    return out_path


