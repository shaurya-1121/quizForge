if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="QuizForge PDF Generator")
    parser.add_argument("--input",  default="pyq_full_database.json", help="JSON question bank")
    parser.add_argument("--exam",   default="ALL",  help="Exam filter: ALL|JEE|NEET|UPSC|CAT|SAT|GK")
    parser.add_argument("--output", default="pdfs", help="Output directory")
    parser.add_argument("--no-cache", action="store_true", help="Force regenerate")
    args = parser.parse_args()

    if not _FPDF_OK:
        print("❌ fpdf2 not installed.  Run:  pip install fpdf2")
        raise SystemExit(1)

    if not os.path.exists(args.input):
        print(f"❌ Input file not found: {args.input}")
        raise SystemExit(1)

    with open(args.input, encoding="utf-8") as f:
        all_questions = json.load(f)

    exam = args.exam.upper()
    if exam != "ALL":
        all_questions = [q for q in all_questions if q.get("exam", "").upper() == exam]

    if not all_questions:
        print(f"⚠️  No questions found for exam={exam}")
        raise SystemExit(0)

    print(f"\n🖨️  Generating PDF for {exam} ({len(all_questions)} questions)…")
    path = generate_pdf(all_questions, exam=exam, output_dir=args.output,
                        use_cache=not args.no_cache)
    print(f"✅ PDF saved → {path}\n")