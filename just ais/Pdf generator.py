"""
╔══════════════════════════════════════════════════════════════╗
║          QUIZFORGE  pdf_generator.py  v1.0                  ║
║          fpdf2 · Chapter-wise PDF · TOC · Answer Key        ║
╚══════════════════════════════════════════════════════════════╝

Usage (standalone):
    pip install fpdf2
    python pdf_generator.py --exam JEE --input pyq_full_database.json

Usage (from Flask):
    from pdf_generator import generate_pdf
    path = generate_pdf(questions, exam="JEE", output_dir="/tmp/quizforge")

Generated PDF structure:
  Page 1   — Cover (exam name, date, question count)
  Page 2   — Table of Contents (auto-built, with page numbers)
  Pages 3+ — Questions grouped by chapter
             MCQ options printed A–D
             Numerical: blank answer box
  Last 2p  — Answer Key (chapter-wise, two-column table)

24-hour cache:
  The cache key is SHA-256(exam + sorted question IDs).
  PDFs are stored in output_dir and reused for 24 h.
"""

from __future__ import annotations

import argparse
import hashlib
import json
import os
import time
from collections import defaultdict
from typing import Optional

try:
    from fpdf import FPDF, XPos, YPos
    _FPDF_OK = True
except ImportError:
    _FPDF_OK = False

# ═══════════════════════════════════════════════════════════════
# COLOURS  (RGB)
# ═══════════════════════════════════════════════════════════════

C_NAVY   = (13,  29,  82)
C_BLUE   = (30,  58, 138)
C_ORANGE = (234, 88,  12)
C_CREAM  = (250, 246, 239)
C_WHITE  = (255, 255, 255)
C_GREY   = (120, 113, 108)
C_DARK   = (28,  25,  23)
C_GREEN  = (21, 128,  61)
C_RED    = (185, 28,  28)

EXAM_COLOURS: dict[str, tuple] = {
    "JEE":  C_BLUE,
    "NEET": C_GREEN,
    "UPSC": C_RED,
    "CAT":  (180, 83, 9),
    "SAT":  (109, 40, 217),
    "GK":   C_ORANGE,
    "ALL":  C_NAVY,
}

CACHE_TTL = 86_400   # 24 hours in seconds


# ═══════════════════════════════════════════════════════════════
# CACHE HELPERS
# ═══════════════════════════════════════════════════════════════

def _cache_key(questions: list[dict], exam: str) -> str:
    ids = sorted(str(q.get("id", q.get("question", "")[:40])) for q in questions)
    raw = exam + "|" + ",".join(ids)
    return hashlib.sha256(raw.encode()).hexdigest()[:16]


def _cached_path(output_dir: str, key: str) -> Optional[str]:
    """Return path if a valid (non-expired) cached PDF exists, else None."""
    path = os.path.join(output_dir, f"quizforge_{key}.pdf")
    if os.path.exists(path):
        age = time.time() - os.path.getmtime(path)
        if age < CACHE_TTL:
            return path
    return None


